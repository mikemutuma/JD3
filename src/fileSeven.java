public class fileSeven {

    public static void main(String[] args) {

        //Loops in JAVA
        int x,y;
        //While Loop
        x = 0;
        while (x<5){
            System.out.println(x);
            x++;
        }

        //Do while
        y = 0;
        do{
            System.out.println(y);
            y++;
        }while (y<5);

        //For Loop
        for (int i = 0; i<5; i++){
            System.out.println(i);
        }

    }

}
